#include "mod_h.h"

float	iso_x(float x, float y, float z, int i)
{
	if (i == 0) // x
		return ((float)(x - y) * cos(0.523599));
	if (i == 1)
		return ((float)(x + y) * sin(0.523599) - z);
	return (0);
}